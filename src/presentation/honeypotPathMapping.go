func findPayloadSpec(
	interceptPath string,
) *honeypotPayloadSpec {
	for specIdx := range honeypotPayloadSpecs {
		if honeypotPayloadSpecs[specIdx].urlPath ==
			interceptPath {
			return &honeypotPayloadSpecs[specIdx]
		}
	}
	return nil
}
